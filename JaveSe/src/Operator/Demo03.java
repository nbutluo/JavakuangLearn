package Operator;

public class Demo03 {
    public static void main(String[] args) {
        // 关系运算符: 返回的结果为true或false

        int a = 10;
        int b = 20;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

        // 取余运算，也叫做取模运算

        int c = 22;

        System.out.println(c%a);
        System.out.println(a%c);
    }


}
