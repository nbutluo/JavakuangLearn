package com.kuang.www.Operator;

public class Demo08 {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        // 字符串连接符
        System.out.println("" + a + b);  //1020

        System.out.println(a + b + ""); // 30

        String s1 = "hello";
        String s2 = 1 + "";
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(333);
    }
}
